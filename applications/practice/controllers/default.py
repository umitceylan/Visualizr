def index():
    if request.vars.csvfile != None:
        # set values
        table = db[request.vars.table]
        file = request.vars.csvfile.file
        
        # import csv file
        table.import_from_csv_file(file)
        
        response.flash = 'Data uploaded'
    return dict()

import json
def get_data():
    rows = db().select(db.friends.id, db.friends.name, db.friends.last_name, limitby=(0, 100), orderby=~db.friends.id)
    return json.dumps([[r.id, r.name, r.last_name] for r in rows])


